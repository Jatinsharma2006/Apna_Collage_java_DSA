//Printing Output in Java 

public class B_PrintingOutput{
    
    public static void main (String args[])
    {
        System.out.print("Hello World!");
        System.out.print('1');
        System.out.print("1");
        System.out.print("1 2 3 4 5");
    }
}
/*
Here 
print is funnction 
Hello world is output we want to print 
; is statement terminator


If we do 
            System.out.print('Hello World');
it will give a error because in java 
'' cant be used for string
and "" is used for string

*/