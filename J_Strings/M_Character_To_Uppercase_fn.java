/*
Character.toUpperCase() fn :

when a character is provided to this fn it change it to Uppercase
Ex:
    Syntax :  Character.toUpperCase( Character_VariableName );
*/
import java.util.*;
public class M_Character_To_Uppercase_fn {
    public static void main (String args[])
    {

        Character ch = 'a';
        ch = Character.toUpperCase(ch);
        System.out.println( ch );

    }
}
