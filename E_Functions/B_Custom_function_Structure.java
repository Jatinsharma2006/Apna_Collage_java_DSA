//Making Custom Function 
/*
Void : Void is the return Type used for Function which do not 
have to retrun anything                                                   */


public class B_Custom_function_Structure{

     public static void PrintHelloWorld()
    {
            System.out.print("Hello World");

            return;
    }

    
    public static void main (String args[])
    {
            PrintHelloWorld();
    }
    
}